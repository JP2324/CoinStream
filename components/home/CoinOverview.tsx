import React from 'react'
import Image from 'next/image'
import { fetcher } from '@/lib/coingecko.actions'
import { CoinOverviewFallback } from './fallback'
import CandlestickChart from '@/components/CandlestickChart'

const CoinOverview = async () => {

    try {
        const [coin, coinOHLCData] = await Promise.all([
            await fetcher<CoinDetailsData>(
                '/coins/bitcoin', {
                dex_pair_format: 'symbol'
            }
            ),
            await fetcher<OHLCData[]>('/coins/bitcoin/ohlc', {
                vs_currency: 'usd',
                days: 1,
                precision: 'full'
            })
        ])

        return (
            <div id='coin-overview'>
                <div className='header pt-2'>
                    <Image src={coin.image.large} alt="Bitcoin"
                        width={56} height={56} />

                    <div className='info'>
                        <p className='name'>{coin.name} / {coin.symbol.toUpperCase()}</p>
                        <h1>$ {coin.market_data.current_price.usd}</h1>
                    </div>
                </div>
                <CandlestickChart data={coinOHLCData} coinId="bitcoin" />
            </div>
        )

    } catch (error) {
        console.error('Error in fetching coin overview', error)
        return <CoinOverviewFallback />
    }

}

export default CoinOverview